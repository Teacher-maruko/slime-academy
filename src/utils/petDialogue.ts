// Generates a daily slime wish.
// Wish is influenced by Zodiac, MBTI, and Diet Preference.
export function generateDailySlimeWish(student: Student): { type: "food" | "learn" | "task" | "achievement"; detail: string; completed: boolean } {
  // Check student's recent food preference
  const foodHistory = student.foodHistory || [];
  let preferredFoodGroup = "水果";
  
  if (foodHistory.length > 0) {
    const counts: Record<string, number> = {};
    foodHistory.forEach(food => {
      counts[food] = (counts[food] || 0) + 1;
    });
    let max = 0;
    Object.entries(counts).forEach(([cat, c]) => {
      if (c > max) {
        max = c;
        preferredFoodGroup = cat;
      }
    });
  }

  // Choose a pool of potential wish types based on zodiac & MBTI
  const zodiac = student.studentZodiac || getZodiac(student.studentBirthday) || "雙子座";
  const mbti = student.slimeData?.mbti || "ISFJ"; // Fallback to ISFJ
  
  const wishTypes: Array<"food" | "learn" | "task" | "achievement"> = ["food", "learn", "task"];
  
  // Fire Signs: Aries, Leo, Sagittarius, and NT types love tasks/achievements
  if (["牡羊座", "獅子座", "射手座"].includes(zodiac) || mbti.includes("NT")) {
    wishTypes.push("task", "achievement", "learn");
  }
  // Earth Signs: Taurus, Virgo, Capricorn, and SJ types love routine tasks and food
  if (["金牛座", "處女座", "摩羯座"].includes(zodiac) || mbti.includes("SJ")) {
    wishTypes.push("food", "task", "learn");
  }
  // Air/Water Signs: Gemini, Libra, Aquarius, Cancer, Scorpio, Pisces etc.
  if (["雙子座", "天秤座", "水瓶座", "巨蟹座", "天蠍座", "雙魚座"].includes(zodiac) || mbti.includes("NF") || mbti.includes("SP")) {
    wishTypes.push("food", "learn", "achievement");
  }

  // Pick a random wish type from the filtered pool
  const chosenType = wishTypes[Math.floor(Math.random() * wishTypes.length)];
  
  let detail = "";
  if (chosenType === "food") {
    detail = `想吃${preferredFoodGroup}`;
  } else if (chosenType === "learn") {
    detail = "想學習一次";
  } else if (chosenType === "task") {
    detail = "想完成任務";
  } else {
    detail = "想獲得成就";
  }

  return {
    type: chosenType,
    detail,
    completed: false
  };
}

